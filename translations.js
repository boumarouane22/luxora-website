// Language management functions
class LanguageManager {
    constructor() {
        this.currentLanguage = 'ar';
        this.init();
    }

    init() {
        // Set initial language from localStorage or default to Arabic
        const savedLanguage = localStorage.getItem('luxora-language') || 'ar';
        this.setLanguage(savedLanguage);
        
        // Setup language selector
        const languageSelect = document.getElementById('languageSelect');
        if (languageSelect) {
            languageSelect.value = this.currentLanguage;
            languageSelect.addEventListener('change', (e) => {
                this.setLanguage(e.target.value);
            });
        }
    }

    setLanguage(lang) {
        if (!translations[lang]) return;
        
        this.currentLanguage = lang;
        localStorage.setItem('luxora-language', lang);
        
        // Update page direction and language attribute
        document.documentElement.lang = lang;
        document.documentElement.dir = lang === 'ar' ? 'rtl' : 'ltr';
        document.body.style.textAlign = lang === 'ar' ? 'right' : 'left';
        
        // Update all elements with data-key attributes
        this.updateContent();
        
        // Update language selector
        const languageSelect = document.getElementById('languageSelect');
        if (languageSelect) {
            languageSelect.value = lang;
        }
    }

    updateContent() {
        const elements = document.querySelectorAll('[data-key]');
        elements.forEach(element => {
            const key = element.getAttribute('data-key');
            const translation = translations[this.currentLanguage][key];
            if (translation) {
                // Update text content
                element.textContent = translation;
            }
        });

        // Update page title
        const titleElement = document.querySelector('title[data-key]');
        if (titleElement) {
            const titleKey = titleElement.getAttribute('data-key');
            const titleTranslation = translations[this.currentLanguage][titleKey];
            if (titleTranslation) {
                document.title = titleTranslation;
            }
        }

        // Update placeholders
        this.updatePlaceholders();
    }

    updatePlaceholders() {
        // Job form textarea placeholder
        const jobTextarea = document.getElementById('jobMessage');
        if (jobTextarea) {
            const placeholderKey = 'form.job.message.placeholder';
            const translation = translations[this.currentLanguage][placeholderKey];
            if (translation) {
                jobTextarea.placeholder = translation;
            }
        }

        // Investment form textarea placeholder
        const investmentTextarea = document.getElementById('investmentMessage');
        if (investmentTextarea) {
            const placeholderKey = 'form.investment.goals.placeholder';
            const translation = translations[this.currentLanguage][placeholderKey];
            if (translation) {
                investmentTextarea.placeholder = translation;
            }
        }
    }

    getCurrentLanguage() {
        return this.currentLanguage;
    }

    translate(key) {
        return translations[this.currentLanguage][key] || key;
    }
}

// Initialize language manager when DOM is loaded
document.addEventListener('DOMContentLoaded', function() {
    window.languageManager = new LanguageManager();
});